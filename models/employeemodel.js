const Sequelize = require('sequelize');
const sequelize = require('./dbconnect');

const employee = sequelize.define('employees', {
  id: {
    type: Sequelize.INTEGER,
    autoIncrement: true,
    primaryKey: true,
  },
  name: {
    type: Sequelize.STRING,
    allowNull: false,
    validate: {
      notEmpty: true,
    },
  },
  email: {
    type: Sequelize.STRING,
    allowNull: false,
    unique: true,
    validate: {
      isEmail: true,
      notEmpty: true,
    },
  },
  password: {
    type: Sequelize.STRING,
    allowNull: false,
    validate: {
      notEmpty: true,
    },
  },
  isEmployee: {
    type: Sequelize.INTEGER,
    defaultValue: 1,
  },
});

module.exports = employee;
